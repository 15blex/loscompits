  <h1>Biografia</h1>
  <p>
    Este grupo se forma en las calles de Barcelona trasladando cada uno de sus integrantes el sabor de sus tierras lanzando un mensaje de alegria y igualdad que no dejará indiferente a nadie.
  </p>
  <p>
